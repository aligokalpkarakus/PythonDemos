# Girilen 3 farklı sayının en büyüğünü bulan algoritma:

sayi1 = int(input("Bir sayı giriniz : "))
sayi2 = int(input("Bir sayı giriniz : "))
sayi3 = int(input("Bir sayı giriniz : "))

if sayi1 > sayi2 and sayi1 > sayi3 :
    print("Birinci sayı en büyükleri.")
if sayi2 > sayi1 and sayi2 > sayi3 :
    print("İkinci sayı en büyükleri.")
if sayi3 > sayi1 and sayi3 > sayi2 :
    print("Üçüncü sayı en büyükleri.")