# Dört büyük takım arasından tuttuğu takımın kısaltmasını bjk gs fb ts olarak alan ve takımın adını yazdıran algoritma:

takim = input("Desteklediğiniz takım : ")

if takim == 'GS' :
    print("Galatasaray")
elif takim == 'FB' :
    print("Fenerbahçe")
elif takim == 'BJK' :
    print("Beşiktaş")
elif takim == 'TS' :
    print("Trabzonspor")
else:
    print("Hatalı giriş yaptınız")